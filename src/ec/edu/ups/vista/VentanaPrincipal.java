    public void conectar() {
        try {
            puertoUSB = new NRSerialPort("COM5", 9600);
            puertoUSB.connect();
            puertoUSB.notifyOnDataAvailable(true);
            puertoUSB.addEventListener(this);

            txtEstado.setText("Puerto USB Listo...");
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    public void desconectar() {
        if (puertoUSB.isConnected()) {
            puertoUSB.disconnect();
            txtEstado.setText("Puerto USB Desconectado.....");
        }
    }

    public void enviarDato(int valor) {
        try {
            OutputStream escritura = puertoUSB.getOutputStream();
            escritura.write(valor);
            escritura.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }

